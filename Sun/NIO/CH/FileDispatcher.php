namespace Sun\NIO\CH;

/**
 * FileDispatcher Class
 *
 * @author Blair
 */
abstract class FileDispatcher extends NativeDispatcher
{

    const NO_LOCK = - 1;

    const LOCKED = 0;

    const RET_EX_LOCK = 1;

    const INTERRUPTED = 2;

    public abstract function truncate($fd, $size);

    public abstract function lock($fd, $blocking);

    public abstract function release($fd);
}
